#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*m;
	size_t	i;

	if (!s)
	{
		s = NULL;
		return ;
	}
	m = (char *)s;
	i = -1;
	while (++i < n)
		*(m + i) = '\0';
}
